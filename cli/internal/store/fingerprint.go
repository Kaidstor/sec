package store

// Отпечаток значения: HMAC-SHA256(мастер-ключ, значение), усечённый до 8 байт (64 бита).
// Позволяет сравнивать/сверять секреты, не раскрывая их. Ключуется мастер-ключом,
// поэтому: (а) безопасен для показа в чате — по отпечатку нельзя перебрать
// значение без мастер-ключа; (б) сравним между сторами только при одном ключе
// (свои же синхронизированные машины), но не с чужим стором.

import (
	"crypto/hmac"
	"crypto/sha256"
	"encoding/hex"
)

// Fingerprint возвращает отпечаток "fp:…" значения под мастер-ключом key.
func Fingerprint(key []byte, value string) string {
	mac := hmac.New(sha256.New, key)
	mac.Write([]byte(value))
	return "fp:" + hex.EncodeToString(mac.Sum(nil)[:8])
}
